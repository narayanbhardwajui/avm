            <h2 class="fw-bold text-primary">One-Unit Residential Appraisal Field Review Report (Form 2000)</h2>
<p>A lender uses this form for the spot-check appraisal component of its quality control process. This appraisal field review report is intended to provide the lender/client with an opinion on the accuracy of the appraisal report under review.</p>
<h3>Scope of Work</h3>
<p>The scope of work for this appraisal field review is defined by the complexity of the appraisal report under review and the reporting requirements of this report form, including the following statement of assumptions and limiting conditions, and certifications. The review appraiser must, at a minimum: (1) read the entire appraisal report under review, (2) perform a visual inspection of the exterior areas of the subject property from at least the street, (3) inspect the neighborhood, (4) inspect each of the comparable sales from at least the street, (5) perform data research and analysis to determine the appropriateness and accuracy of the data in the appraisal report, (6) research, verify, and analyze data from reliable public and/or private sources, (7) determine the accuracy of the opinion of value, and (8) assume the property condition reported in the appraisal report is accurate unless there is evidence to the contrary.</p>
<p>If the review appraiser determines that the opinion of value in the report under review is not accurate, he or she is required to provide an opinion of market value. The review appraiser is not required to replicate the steps completed by the original appraiser that the review appraiser believes to be reliable and in compliance with the applicable real property appraisal development standards of the Uniform Standards of Professional Appraisal Practice. Those items in the appraisal report under review are extended to this report by the use of an extraordinary assumption, which is identified in Section II, Question 2. If the review appraiser determines that the opinion of value is not accurate, he or she must present additional data that has been researched, verified, and analyzed to produce an accurate opinion of value in accordance with the applicable sections of Standard 1 of the Uniform Standards of Professional Appraisal Practice.</p>
<h3>Required Exhibits</h3>
<p>A street map that shows the location of the subject property and of all comparables included in the appraisal report under review and any additional comparable sales provided by the review appraiser;</p>
<p>Clear, descriptive photographs (either in black and white or color) that show the front, back, and a street scene of the subject property, and that are appropriately identified. (Photographs must be originals that are produced either by photography or electronic imaging);</p>
<p>Clear, descriptive photographs (either in black and white or color) that show the front of each comparable sale included in the appraisal report under review and any additional comparable sales described in the appraisal field review report. Generally, photographs should be originals that are produced by photography or electronic imaging; however, copies of photographs from a multiple listing service or from the appraiserâ€™s files are acceptable if they are clear and descriptive.</p>

<p>This appraisal report is subject to the scope of work, intended use, intended user, definition of market value, statement of assumptions and limiting conditions, and certifications contained in the report form. Modifications, additions, or deletions to the intended use, intended user, definition of market value, or assumptions and limiting conditions are not permitted. The appraiser may expand the scope of work to include any additional research or analysis necessary based on the complexity of this appraisal assignment. Modifications or deletions to the certifications are also not permitted. However, additional certifications that do not constitute material alterations to this appraisal report, such as those required by law or those related to the appraiser’s continuing education or membership in an appraisal organization are permitted.</p>
<h3>Scope of Work</h3>
<p>The scope of work for this appraisal is defined by the complexity of this appraisal assignment and the reporting requirements of this appraisal report form, including the following definition of market value, statement of assumptions and limiting conditions, and certifications. The appraiser must, at a minimum: (1) perform a visual inspection of the exterior areas of the subject property from at least the street, (2) inspect the neighborhood, (3) inspect each of the comparable sales from at least the street, (4) research, verify, and analyze data from reliable public and/or private sources, and (5) report his or her analysis, opinions, and conclusions in this appraisal report.</p>
<p>The appraiser must be able to obtain adequate information about the physical characteristics (including, but not limited to, condition, room count, gross living area, etc.) of the subject property from the exterior-only inspection and reliable public and/or private sources to perform this appraisal. The appraiser should use the same type of data sources that he or she uses for comparable sales such as, but not limited to, multiple listing services, tax and assessment records, prior inspections, appraisal files, information provided by the property owner, etc.</p>

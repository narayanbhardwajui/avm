            <h2 class="fw-bold text-primary">Exterior-Only Inspection Individual Cooperative Interest Appraisal Report (Form 2095)</h2>
<p>This report form is designed to report an appraisal of the cooperative interest (the cooperative shares or other evidence of an ownership interest in the cooperative corporation and the accompanying occupancy rights) in a cooperative project or the cooperative interest in a planned unit development (PUD). This form is not designed to report an appraisal of a manufactured home or a unit in a condominium project.</p>
<h3>Modifications, Additions, or Deletions</h3>
<p>This appraisal report is subject to the following scope of work, intended use, intended user, definition of market value, statement of assumptions and limiting conditions, and certifications. Modifications, additions, or deletions to the intended use, intended user, definition of market value, or assumptions and limiting conditions are not permitted. The appraiser may expand the scope of work to include any additional research or analysis necessary based on the complexity of this appraisal assignment. Modifications or deletions to the certifications are also not permitted. However, additional certifications that do not constitute material alterations to this appraisal report, such as those required by law or those related to the appraiser’s continuing education or membership in an appraisal organization are permitted.</p>
<h3>Scope of Work</h3>
<p>The scope of work for this appraisal is defined by the complexity of this appraisal assignment and the reporting requirements of this appraisal report form, including the following definition of market value, statement of assumptions and limiting conditions, and certifications. The appraiser must, at a minimum: (1) perform a visual inspection of the exterior areas of the subject unit from at least the street, (2) inspect and analyze the cooperative project, (3) inspect the neighborhood, (4) inspect each of the comparable sales from at least the street, (5) research, verify, and analyze data from reliable public and/or private sources, and (6) report his or her analysis, opinions, and conclusions in this appraisal report.</p>
<h3>New Projects</h3>
<p>For units in new (or recently converted) cooperative projects, the appraiser must compare the subject property to other properties in its general market area as well as to properties within the subject project. This comparison should help demonstrate market acceptance of new developments and the properties within them. Generally, the appraiser should select one comparable sale from the subject project, one comparable sale from outside the subject project, and one other comparable sale, which can be from inside or outside of the subject project, that the appraiser considers to be a good indicator of value for the subject property. In selecting the comparables, the appraiser should keep in mind that re-sales from within the subject project are preferable to sales from outside the project as long as the developer or builder of the subject property is not involved in the transactions.</p>
<h3>Established Projects</h3>
<p>For units in established cooperative projects (those that have resale activity), the appraiser should use comparable sales from within the subject project if there are any available. Resale activity from within the subject project should be the best indicator of value for properties in that project. If the appraiser uses sales of comparable properties that are located outside of the subject neighborhood, he or she must include an explanation with the analysis.</p>
<h3>Required Exhibits</h3>
<ul>
<li>A street map that shows the location of the subject property and of all comparables that the appraiser used;</li>
<li>Clear, descriptive photographs (either in black and white or color) that show the front of the subject property, and that are appropriately identified. (Photographs must be originals that are produced either by photography or electronic imaging);</li>
<li>Any other data–as an attachment or addendum to the appraisal report form–that are necessary to provide an adequately supported opinion of market value.</li>
</ul>
